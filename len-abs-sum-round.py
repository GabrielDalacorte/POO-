"""
Len, Abs, Sum, Round

# Len

len() - Retorna o tamanho (ou seja, o número de itens) de um iterável.


# Só para revisar

print(len('Geek University'))

# Abs

abs() - Retorna o valor absoluto de um número inteiro ou real. De forma básica
seria o seu valor real sem o sinal.

OBS = Não existe "Abs()" para uma string.
# Exemplo Abs
print(abs(-2))
print(abs(-3.12))


# Sum

sum() - Retorna como parametro um iteravel, podendo receber um valor inicial, e
retornar a soma total dos elementos.

print(sum([1, 2, 42, 4]))


# Round

round() - Retorna um numero arredondado para N digito de precisão apos a casa
decimal. Se a precisão não for informada retorna o inteiro mais proximo de entrada.

"""

print(round(10.2))
print(round(10.6))
print(round(1.2323213, 2))

