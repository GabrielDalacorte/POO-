"""
O tipo de dado none em python representa o time sem tem Type ou poderia ser conhecido tambem, como tipo
vazio. Porem falar um tipo sem tipo e mais apropriado.

OBS: O tipo None e sempre especificado com a primeira letra maiúscila
Certo = None // Errado = none

Quando podemos utilizar o tipo None:
    - Podemos utilizar None, quando queremos criar uma variável e inicializa-la com um tipo,
    sem tipo. Antes de recebermos um valor final.


O tipo None em python é sempre considerado Falso (false)
"""

numeros = None
print(type(numeros))

numeros = 1.34, 4.33, 5.32

print(numeros)
print(type(numeros))
